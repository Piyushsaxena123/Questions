public class Operators {
    public static void main(String[] args) {
        System.out.println("ARITHMETIC OPERATORD");
        int p = 20, q = 10;
        System.out.println(p + q);
        System.out.println(p - q);
        System.out.println(p * q);
        System.out.println(p / q);
        System.out.println(p % q);
    }
}
