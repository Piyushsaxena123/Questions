public class Relational {
    public static void main(String[] args) {
        System.out.println("RELATIONAL OPERATORS");
        int p = 10, q = 15;
        System.out.println(p == q); // false
        System.out.println(p != q); // true
        System.out.println(p > q); // false
        System.out.println(p < q); // true
        System.out.println(p >= q); // false
        System.out.println(p <= q); // true
    }
}
